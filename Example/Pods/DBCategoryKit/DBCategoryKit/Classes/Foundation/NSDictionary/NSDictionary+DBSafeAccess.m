@implementation NSMutableDictionary (DBSafeAccess)

- (void)db_setObj:(id)i forKey:(NSString*)key{
    if (i!=nil) {
        self[key] = i;
    }
}

- (void)db_setString:(NSString*)i forKey:(NSString*)key;
{
    [self setValue:i forKey:key];
}

